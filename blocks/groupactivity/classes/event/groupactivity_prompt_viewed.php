namespace block_groupactivity\event;

defined('MOODLE_INTERNAL') || die();

/**
 * The block_groupactivity user notification viewed event class.
 */
class groupactivity_prompt_viewed extends \core\event\base {

    /**
     * Init method.
     *
     * @return void
     */
    protected function init() {
        $this->data['crud'] = 'r';
        $this->data['edulevel'] = self::LEVEL_OTHER;
    }

    /**
     * Returns description of what happened.
     *
     * @return string
     */
    public function get_description() {
        return "The user with id '$this->userid' has viewed the changed prompt from groupactivity block.";
    }

    /**
     * Return localised event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('eventusernotificationviewed', 'block_groupactivity');
    }
}